'''10. Matrices.
Escribir unha función que reciba duas matrices e devolte a suma.
Escribir unha función que reciba duas matrices e devolte o produto.
'''

def sumar(matriz1, matriz2):
    return sum(matriz1) + sum(matriz2)

def produto(matriz1, matriz2):
    return sum(matriz1) * sum(matriz2)

print(sumar([1, 2, 3], [4, 5, 6]))
print(produto([1, 2, 3], [4, 5, 6]))